package by.jonline.mod01.linear.main;

// (a/c * b/d) - ((a*b - c) / c*d) 

public class Task06 {

	public static void main(String[] args) {
		
		double a = 2.5;
		double b = 12.0;
		double c = 5.0;
		double d = 1.0;
		
		double res = (a / c * b / d) - ((a * b - c) / c * d);
		
		System.out.println(res);
			

	}

}
